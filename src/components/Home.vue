<template>
    <ul>
      <li v-for="note in notes">
        <h3>{{ note.date | moment 'MMM D, YYYY' }}</h3>
        <h2><a v-link="note.src">{{ note.title }}</a></h2>
      </li>
    </ul>
</template>

<script>
  export default {
    data () {
      return {
        notes: []
      }
    },
    ready () {
      this.$http.get('http://localhost:8888/home').then(function (ret) {
        this.$set('notes', ret.data)
        for (var i = 0, l = this.notes.length; i < l; i++) {
          this.notes[i].src = '/note/' + this.notes[i]._id
        }
      }, function (err) {
        console.log(err)
      })
    }
  }
</script>

